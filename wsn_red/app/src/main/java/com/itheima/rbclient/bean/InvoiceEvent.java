package com.itheima.rbclient.bean;

/**
 * 发票的event类
 */
public class InvoiceEvent implements IEvent {
    /**
     * 发票抬头
     */
    public String ttDes;
    /**
     * 发票内容
     */
    public int content;

    public InvoiceEvent(String ttDes, int content) {
        this.ttDes = ttDes;
        this.content = content;
    }
}
